package Day66;

public class positiveandnegative {
    public static void main(String[] args) {
        int num=5;

        if(num>0){
            System.out.println("The num is Positive: "+num);
        }
        else if(num<0){
            System.out.println("The num is Negative: "+num);
        }
        else
        System.out.println("The num is zero: "+num);
    }
}
